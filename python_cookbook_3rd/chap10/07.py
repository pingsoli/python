# 10.7 Making a Directory or Zip File Runnable As a Main Script

# You can add a __main__.py file
'''
myapplication/
    spam.py
    bar.py
    grok.py
    __main__.py
'''

# Then you can simply use
# $ python myapplication
# The interpreter # Then you can simply call 
# $ python myapplication will execute the __main__.py file 
# as the main program.
